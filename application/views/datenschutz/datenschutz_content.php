        </header>
        <p>
            Cookies sind kleine Textdateien, die im Speicher Ihres Gerätes mit dem Sie das Internet nutzen, dem
            von Ihnen genutzten Browser zugeordnet abgespeichert werden. Um die Nutzung unserer Web-Seiten
            effektiver und nutzerfreundlicher zu gestalten, werden bei Ihrem Besuch unserer Web-Seiten Cookies
            auf Ihrem Gerät gespeichert, wodurch uns ebenfalls (abhängig vom Cookie) Informationen zufließen
            können. Cookies können aber keine Programme ausführen oder Schadsoftware auf Ihr Gerät übertragen.
        </p>
        <p>
            Unsere Web-Seite nutzt
            Transiente Cookies; solche werden automatisch nach Schließung des Browsers oder durch „ausloggen“
            gelöscht. Dazu zählen insbesondere Session-Cookies, womit eine sogenannte Session-ID, mit welcher
            sich verschiedene Anfragen Ihres Browsers der gemeinsamen Sitzung zuordnen lassen, gespeichert
            werden. Aufgrund dessen kann Ihr Gerät/Rechner wiedererkannt werden, wenn auf die Web-Seite wieder
            zugegriffen wird.
